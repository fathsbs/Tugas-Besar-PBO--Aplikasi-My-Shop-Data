    private void barpanelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_barpanelMouseClicked
        if (evt.getClickCount() == 2 &&!evt.isConsumed())
        {
            if (AboutView.this.getExtendedState()==MAXIMIZED_BOTH)
            {
                AboutView.this.setExtendedState(JFrame.NORMAL);
            }
            else
            {
                AboutView.this.setExtendedState(WIDTH);
            }
        }
    }//GEN-LAST:event_barpanelMouseClicked

